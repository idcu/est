    private static void basicChainQueryExample(Orm orm) {
        System.out.println("\n--- 方式一：基础链式查询 ---");
        System.out.println("\n【特点】");
        System.out.println("  - 简洁的链式调用语法");
        System.out.println("  - 支持多种条件组合");
        System.out.println("  - 支持排序、分页等操作\n");
        
        prepareTestData(orm);
        
        System.out.println("示例 1: 查询技术部所有用户");
        List<User> techUsers = orm.query(User.class)
                .eq("department", "技术部")
                .orderByDesc("salary")
                .get();
        System.out.println("   结果:");
        for (User user : techUsers) {
            System.out.println("     - " + user);
        }
        
        System.out.println("\n示例 2: 查询年龄大于 30 且薪资大于 18000 的用户");
        List<User> highSalaryUsers = orm.query(User.class)
                .gt("age", 30)
                .and()
                .gt("salary", 18000.0)
                .get();
        System.out.println("   结果:");
        for (User user : highSalaryUsers) {
            System.out.println("     - " + user);
        }
        
        System.out.println("\n示例 3: 查询姓名包含 \"张\" 的用户");
        List<User> zhangUsers = orm.query(User.class)
                .like("name", "张")
                .get();
        System.out.println("   结果:");
        for (User user : zhangUsers) {
            System.out.println("     - " + user);
        }
        
        System.out.println("\n示例 4: 查询单个用户");
        User firstUser = orm.query(User.class)
                .orderByAsc("age")
                .getOne();
        System.out.println("   最年轻的用户: " + firstUser);
        
        System.out.println("\n示例 5: 统计技术部人数");
        long techCount = orm.query(User.class)
                .eq("department", "技术部")
                .count();
        System.out.println("   技术部人数: " + techCount);
        
        System.out.println("\n示例 6: 查询技术部最高薪资");
        Object maxSalary = orm.query(User.class)
                .eq("department", "技术部")
                .max("salary");
        System.out.println("   技术部最高薪资: " + maxSalary);
        
        logger.info("基础链式查询示例完成");
    }

    private static void lambdaQueryExample(Orm orm) {
        System.out.println("\n--- 方式二：Lambda 表达式查询（类型安全）---");
        System.out.println("\n【特点】");
        System.out.println("  - 类型安全，编译时检查");
        System.out.println("  - 使用方法引用，避免拼写错误");
        System.out.println("  - IDE 自动补全支持");
        System.out.println("  - 重构更安全\n");
        
        System.out.println("注意：MemoryOrm 暂不支持 Lambda 查询，以下为 JDBC 模式下的示例代码");
        System.out.println("\n示例代码（JDBC 模式）:");
        System.out.println("   // 查询技术部薪资大于 15000 的用户，按薪资降序排序");
        System.out.println("   List<User> users = orm.lambdaQuery(User.class)");
        System.out.println("           .eq(User::getDepartment, \"技术部\")");
        System.out.println("           .gt(User::getSalary, 15000.0)");
        System.out.println("           .orderByDesc(User::getSalary)");
        System.out.println("           .get();");
        System.out.println();
        System.out.println("   // 查询单个用户");
        System.out.println("   User user = orm.lambdaQuery(User.class)");
        System.out.println("           .eq(User::getName, \"张三\")");
        System.out.println("           .getOne();");
        System.out.println();
        System.out.println("   // 统计产品部人数");
        System.out.println("   long count = orm.lambdaQuery(User.class)");
        System.out.println("           .eq(User::getDepartment, \"产品部\")");
        System.out.println("           .count();");
        
        logger.info("Lambda 查询示例完成");
    }

