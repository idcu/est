    private static void paginationExample(Orm orm) {
        System.out.println("\n--- 方式四：分页查询 ---");
        System.out.println("\n【特点】");
        System.out.println("  - 自动计算总页数");
        System.out.println("  - 支持任意查询条件");
        System.out.println("  - 返回完整的分页信息\n");
        
        System.out.println("示例 1: 查询第 1 页，每页 3 条记录");
        Page<User> page1 = orm.page(User.class, new Page<>(1, 3));
        System.out.println("   第 " + page1.getPageNum() + " 页 / 共 " + page1.getPages() + " 页");
        System.out.println("   总记录数: " + page1.getTotal());
        System.out.println("   本页数据:");
        for (User user : page1.getRecords()) {
            System.out.println("     - " + user);
        }
        
        System.out.println("\n示例 2: 按部门筛选并分页（技术部，第 2 页，每页 2 条）");
        Page<User> page2 = orm.page(User.class, new Page<>(2, 2), 
                orm.query(User.class).eq("department", "技术部"));
        System.out.println("   第 " + page2.getPageNum() + " 页 / 共 " + page2.getPages() + " 页");
        System.out.println("   总记录数: " + page2.getTotal());
        System.out.println("   本页数据:");
        for (User user : page2.getRecords()) {
            System.out.println("     - " + user);
        }
        
        logger.info("分页查询示例完成");
    }

    private static void batchOperationsExample(Orm orm) {
        System.out.println("\n--- 方式五：批量操作 ---");
        System.out.println("\n【特点】");
        System.out.println("  - 批量插入/更新/删除");
        System.out.println("  - 减少数据库交互次数");
        System.out.println("  - 提高性能\n");
        
        System.out.println("示例 1: 批量插入新用户");
        List<User> newUsers = Arrays.asList(
            new User("郑十一", 26, "zheng11@example.com", 13500.0, "技术部"),
            new User("王十二", 31, "wang12@example.com", 19000.0, "产品部")
        );
        List<User> savedUsers = orm.saveBatch(newUsers);
        System.out.println("   批量插入了 " + savedUsers.size() + " 个用户");
        for (User user : savedUsers) {
            System.out.println("     - " + user);
        }
        
        System.out.println("\n示例 2: 批量更新用户");
        savedUsers.get(0).setSalary(14000.0);
        savedUsers.get(1).setSalary(19500.0);
        List<User> updatedUsers = orm.updateBatchById(savedUsers);
        System.out.println("   批量更新了 " + updatedUsers.size() + " 个用户");
        for (User user : updatedUsers) {
            System.out.println("     - " + user);
        }
        
        System.out.println("\n当前用户总数: " + orm.count(User.class));
        
        System.out.println("\n示例 3: 批量删除刚才插入的用户");
        List<Object> idsToDelete = Arrays.asList(
            savedUsers.get(0).getId(),
            savedUsers.get(1).getId()
        );
        int deletedCount = orm.removeByIds(User.class, idsToDelete);
        System.out.println("   批量删除了 " + deletedCount + " 个用户");
        
        System.out.println("\n当前用户总数: " + orm.count(User.class));
        
        logger.info("批量操作示例完成");
    }
